"""Wrapper code installed into VLMEvalKit for PEFT adapter evaluation."""

from __future__ import annotations


WRAPPER_MODULE = r'''
"""SafeNav Qwen2-VL wrapper for VLMEvalKit.

This file is generated by scripts/08_prepare_vlmevalkit_utility_eval.py.
It keeps VLMEvalKit evaluation independent from the safety-unlearning codebase
while allowing base and PEFT-adapter models to share the same Qwen2-VL backend.
"""

import os

import torch
from vlmeval.vlm.qwen2_vl.model import Qwen2VLChat


class SafeNavQwen2VLChat(Qwen2VLChat):
    """Qwen2VLChat plus an optional PEFT adapter loaded after the base model."""

    def __init__(
        self,
        model_path,
        adapter_path=None,
        merge_adapter=False,
        attn_implementation="sdpa",
        device_map="auto",
        print_device_map=True,
        **kwargs,
    ):
        patched = []
        if attn_implementation not in (None, "", "none", "None"):
            import transformers

            candidate_names = [
                "Qwen2VLForConditionalGeneration",
                "Qwen2_5_VLForConditionalGeneration",
                "Qwen2_5OmniForConditionalGeneration",
            ]
            for cls_name in candidate_names:
                cls = getattr(transformers, cls_name, None)
                if cls is None or not hasattr(cls, "from_pretrained"):
                    continue
                original = cls.from_pretrained

                def patched_from_pretrained(*args, _original=original, **inner_kwargs):
                    inner_kwargs["attn_implementation"] = attn_implementation
                    if device_map not in (None, "", "none", "None"):
                        inner_kwargs["device_map"] = device_map
                    return _original(*args, **inner_kwargs)

                setattr(cls, "from_pretrained", patched_from_pretrained)
                patched.append((cls, original))

        try:
            super().__init__(model_path=model_path, **kwargs)
        finally:
            for cls, original in patched:
                setattr(cls, "from_pretrained", original)

        if adapter_path in (None, "", "none", "None", "null", "NULL"):
            self.adapter_path = None
            if print_device_map:
                self._print_device_diagnostics()
            return

        adapter_path = os.path.abspath(os.path.expanduser(str(adapter_path)))
        if not os.path.exists(adapter_path):
            raise FileNotFoundError(f"PEFT adapter_path does not exist: {adapter_path}")

        try:
            from peft import PeftModel
        except Exception as exc:
            raise RuntimeError("SafeNavQwen2VLChat requires `peft` to load adapters.") from exc

        self.adapter_path = adapter_path
        self.model = PeftModel.from_pretrained(self.model, adapter_path, is_trainable=False)
        if merge_adapter:
            self.model = self.model.merge_and_unload()
        self.model.eval()
        torch.cuda.empty_cache()
        if print_device_map:
            self._print_device_diagnostics()

    def _print_device_diagnostics(self):
        try:
            first_device = next(self.model.parameters()).device
        except Exception:
            first_device = "unknown"
        hf_device_map = getattr(self.model, "hf_device_map", None)
        print(
            "[SafeNavQwen2VLChat] "
            f"cuda_available={torch.cuda.is_available()} "
            f"first_param_device={first_device} "
            f"hf_device_map={hf_device_map}"
        )
'''


def wrapper_module_text() -> str:
    return WRAPPER_MODULE.lstrip()
